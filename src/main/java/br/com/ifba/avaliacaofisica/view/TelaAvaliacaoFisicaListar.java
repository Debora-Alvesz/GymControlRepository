    private void btnBusMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusMatActionPerformed
        // 1. Abre uma caixinha para o usuário digitar a matricula
    String matDigitada = javax.swing.JOptionPane.showInputDialog(this, "Digite a matricula do Aluno:");

    if (matDigitada != null && !matDigitada.trim().isEmpty()) {
        try {
            // 2. Busca a avaliação fisica via Controller
            br.com.ifba.avaliacaofisica.entity.AvaliacaoFisica e = avaliacaoFisicaController.findByAluno_Matricula(matDigitada);

            if (e != null) {
                // 3. Limpa a tabela e mostra apenas o resultado encontrado
                modeloTabela.setNumRows(0);
                modeloTabela.addRow(new Object[]{
                e.getAluno().getMatricula(),
                e.getInstrutor().getName(),
                e.getDataAvaliacao(),
                e.getPeso(),
                e.getAltura(),
                e.getPercentualGordura(),
                e.getBiceps(),
                e.getCintura(),
                e.getOmbro(),
                e.getPanturrilha(),
                e.getCoxa(),
                e.getGluteo(),
                e.getPeitoral(),
                avaliacaoFisicaController.calcularImc(e.getAltura(), e.getPeso())
                });
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Avaliação Fisica não encontrada!");
            }
            
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao buscar: " + ex.getMessage());
        }
    } else if (matDigitada != null) {
        // Se o usuário clicou OK sem digitar nada, voltamos a listar todos
        atualizarTabela();
    }
    }//GEN-LAST:event_btnBusMatActionPerformed

