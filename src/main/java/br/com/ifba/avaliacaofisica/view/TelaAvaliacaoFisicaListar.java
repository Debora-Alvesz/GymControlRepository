    public void atualizarTabela() {
    // Limpa a tabela antes de carregar
    modeloTabela.setNumRows(0);
    
   try {
        // Busca a lista no banco via controller
        java.util.List<br.com.ifba.avaliacaofisica.entity.AvaliacaoFisica> lista = avaliacaoFisicaController.findAll();
        
        // Adiciona cada exercício como uma linha na tabela
        for (br.com.ifba.avaliacaofisica.entity.AvaliacaoFisica e : lista) {
            modeloTabela.addRow(new Object[]{
                e.getAluno().getMatricula(),
                e.getInstrutor().getName(),
                e.getDataAvaliacao(),
                e.getPeso(),
                e.getAltura(),
                e.getPercentualGordura(),
                e.getBiceps(),
                e.getCintura(),
                e.getOmbro(),
                e.getPanturrilha(),
                e.getCoxa(),
                e.getGluteo(),
                e.getPeitoral(),
                avaliacaoFisicaController.calcularImc(e.getAltura(), e.getPeso())
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar exercícios: " + e.getMessage());
    }
 }
    
