    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //Verifica se tem alguma linha selecionada
    int linhaSelecionada = tblAvaliacaoFisica.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione uma avaliação fisica na tabela para excluir.");
        return;
    }

    //Pega o ID da primeira coluna (coluna 0)
    String matricula = (String) modeloTabela.getValueAt(linhaSelecionada, 0);

    //Pergunta se tem certeza
    int resposta = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja realmente excluir a avaliação fisica MATRICULA: " + matricula + "?");
    
    if (resposta == javax.swing.JOptionPane.YES_OPTION) {
        try {
            avaliacaoFisicaController.deleteByMatricula(matricula); // Chama o controller
            javax.swing.JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
            atualizarTabela(); // Atualiza a lista automaticamente
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

