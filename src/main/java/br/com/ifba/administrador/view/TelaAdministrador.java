    public void atualizarTabela() {
    // Limpa a tabela antes de carregar
    modeloTabela.setNumRows(0);
    
   try {
        // Busca a lista no banco via controller
        java.util.List<br.com.ifba.administrador.entity.Administrador> lista = administradorController.findAll();
        
        // Adiciona cada exercício como uma linha na tabela
        for (br.com.ifba.administrador.entity.Administrador e : lista) {
            modeloTabela.addRow(new Object[]{
                e.getCpf(),
                e.getNome(),
                e.getTelefone(),
                e.getNascimento(),
                e.getEmail(),
                e.getEndereco(),
                
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar administradores: " + e.getMessage());
    }
 }
