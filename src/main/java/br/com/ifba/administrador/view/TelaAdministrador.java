    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //Verifica se tem alguma linha selecionada
    int linhaSelecionada = tblAdministrador.getSelectedRow();
    
    if (linhaSelecionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Selecione um administrador na tabela para excluir.");
        return;
    }

    //Pega o cpf da primeira coluna (coluna 0)
    String cpf = (String) modeloTabela.getValueAt(linhaSelecionada, 0);

    //Pergunta se tem certeza
    int resposta = javax.swing.JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o administrador CPF: " + cpf + "?");
    
    if (resposta == javax.swing.JOptionPane.YES_OPTION) {
        try {
            administradorController.deleteByCpf(cpf); // Chama o controller
            javax.swing.JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
            atualizarTabela(); // Atualiza a lista automaticamente
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // 1. Abre uma caixinha para o usuário digitar o CPF
    String cpfDigitado = javax.swing.JOptionPane.showInputDialog(this, "Digite o CPF do Administrador:");

    if (cpfDigitado != null && !cpfDigitado.trim().isEmpty()) {
        try {
            // 2. Busca Administrador via Controller
            br.com.ifba.administrador.entity.Administrador e = administradorController.findByCpf(cpfDigitado);

            if (e != null) {
                // 3. Limpa a tabela e mostra apenas o resultado encontrado
                modeloTabela.setNumRows(0);
                modeloTabela.addRow(new Object[]{
                e.getCpf(),
                e.getNome(),
                e.getTelefone(),
                e.getNascimento(),
                e.getEmail(),
                e.getEndereco()
                });
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Administrador não encontrado!");
            }
            
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao buscar: " + ex.getMessage());
        }
    } else if (cpfDigitado != null) {
        // Se o usuário clicou OK sem digitar nada, voltamos a listar todos
        atualizarTabela();
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

